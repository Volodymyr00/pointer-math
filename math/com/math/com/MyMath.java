package math.com;

public class MyMath {
    public static final int a = 10;

    public static double radius(double r) {
        final double PI = 3.141592;
        return 2 * PI * r;
    }


    public static double summa(double r) {
        final double PI = 3.141592;
        return (r * r) * PI;
    }

    public static int mult(int a) {
        return a * a;
    }
}
